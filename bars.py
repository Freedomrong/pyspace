"""
Bars Module
=================
这是一个打印不同分割线的事例模块
"""
def starbar(num):
    """打印 * 的分割线

    :arg num: 线长
    """
    print('*' * num)#打印num个数量的 * 号

def hashbar(num):
    """打印 # 的分割线

    :arg num: 线长
    """
    print('#' * num)

def simplebar(num):
    """打印 - 的分割线

    :arg num: 线长
    """
    print('-' * num)
